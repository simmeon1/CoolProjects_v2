    private record Cell(int I, int J, char? Char);

    private int GetResult(string str, bool mas = false)
    {
        var input = str.Split("\n").Select(s => s.Trim());
        var map = input.Select(l => l.Select(c => c).ToList()).ToList();
        Cell GetCharAt(int i, int j) =>
            new(i, j, i >= 0 && i <= map.Count - 1 && j >= 0 && j <= map[i].Count - 1 ? map[i][j] : null);
        
        var total = 0;
        for (int i = 0; i < map.Count; i++)
        {
            List<char> lines = map[i];
            for (int j = 0; j < lines.Count; j++)
            {
                char letter = lines[j];
                if (letter != (mas ? 'A' : 'X'))
                {
                    continue;
                }

                if (mas)
                {
                    var chars = new List<Cell>
                    {
                        GetCharAt(i - 1, j - 1),
                        GetCharAt(i - 1, j + 1),
                        GetCharAt(i + 1, j - 1),
                        GetCharAt(i + 1, j + 1)
                    };
                    
                    var ordered = chars.OrderBy(x => x.Char).ToList();
                    if (ordered.Select(x => x.Char).SequenceEqual(['M', 'M', 'S', 'S']) 
                        && (ordered[0].I == ordered[1].I || ordered[0].J == ordered[1].J)
                    )
                    {
                        total++;
                    }
                }
                else
                {
                    List<List<Cell>> charsList = [
                        new() { GetCharAt(i + 0, j + 1), GetCharAt(i + 0 , j + 2), GetCharAt(i + 0, j + 3) },
                        new() { GetCharAt(i + 1, j + 1), GetCharAt(i + 2 , j + 2), GetCharAt(i + 3, j + 3) },
                        new() { GetCharAt(i + 1, j + 0), GetCharAt(i + 2 , j + 0), GetCharAt(i + 3, j + 0) },
                        new() { GetCharAt(i + 1, j - 1), GetCharAt(i + 2 , j - 2), GetCharAt(i + 3, j - 3) },
                        new() { GetCharAt(i - 0, j - 1), GetCharAt(i - 0 , j - 2), GetCharAt(i - 0, j - 3) },
                        new() { GetCharAt(i - 1, j - 1), GetCharAt(i - 2 , j - 2), GetCharAt(i - 3, j - 3) },
                        new() { GetCharAt(i - 1, j - 0), GetCharAt(i - 2 , j - 0), GetCharAt(i - 3, j - 0) },
                        new() { GetCharAt(i - 1, j + 1), GetCharAt(i - 2 , j + 2), GetCharAt(i - 3, j + 3) },
                    ];

                    foreach (var chars in charsList)
                    {
                        if (chars[0].Char == 'M' && chars[1].Char == 'A' && chars[2].Char == 'S')
                        {
                            total++;
                        }
                    }
                }
                
            }
        }
        return total;
    }

